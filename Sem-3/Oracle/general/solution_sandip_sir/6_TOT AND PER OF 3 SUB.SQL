/***********TOTAL AND PERCENTAGE OF THREE SUBJECT***********/

DECLARE
	VB NUMBER(3);
	ORACLE NUMBER(3);
	PHP NUMBER(3);
	TOTAL NUMBER(3);
	PER NUMBER(5,2);
BEGIN
	VB:=&VB;
	ORACLE:=&ORACLE;
	PHP:=&PHP;
	TOTAL:=VB+ORACLE+PHP;
	PER:=TOTAL/3;
	DBMS_OUTPUT.PUT_LINE('TOTAL MARKS = ' ||TOTAL);
	DBMS_OUTPUT.PUT_LINE('PERCENTAGE = ' ||PER);
END;
