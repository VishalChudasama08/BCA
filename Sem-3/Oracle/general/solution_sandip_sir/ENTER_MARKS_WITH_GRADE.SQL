DECLARE
	V_GRNO NUMBER(3);
	V_SUB1 NUMBER(3);
	V_SUB2 NUMBER(3);
	V_SUB3 NUMBER(3);
	V_TOTAL NUMBER(3);
	V_PER NUMBER(5,2);
	V_GRADE VARCHAR2(15);
BEGIN
	V_GRNO:=&V_GRNO;
	V_SUB1:=&V_SUB1;
	V_SUB2:=&V_SUB2;
	V_SUB3:=&V_SUB3;
	V_TOTAL:=V_SUB1+V_SUB2+V_SUB3;
	
	
	IF V_SUB1>=40 AND V_SUB2>=40 AND V_SUB3>=40 THEN
		V_PER:=V_TOTAL/3;
	ELSE
		V_PER:=0;
	END IF;

	IF V_PER>=40 AND V_PER<50 THEN
		V_GRADE:='PASS';
	ELSIF V_PER>=50 AND V_PER<60 THEN
		V_GRADE:='SECOND';	
	ELSIF V_PER>=60 AND V_PER<70 THEN
		V_GRADE:='FIRST';
	ELSIF V_PER>70 THEN
		V_GRADE:='DISTINCTION';	
	ELSE
		V_GRADE:='FAIL';
	END IF;

	INSERT INTO marks (GRNO,SUB1,SUB2,SUB3,TOTAL,PER,GRADE)
	VALUES
	(V_GRNO,V_SUB1,V_SUB2,V_SUB3,V_TOTAL,V_PER,V_GRADE);
END;
