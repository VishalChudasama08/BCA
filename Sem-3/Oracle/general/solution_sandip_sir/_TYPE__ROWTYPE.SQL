CREATE TABLE STUD_MALE
(GRNO NUMBER(3),
NAME VARCHAR2(30),
AGE NUMBER(3));

CREATE TABLE STUD_FEMALE
(GRNO NUMBER(3),
NAME VARCHAR2(30),
AGE NUMBER(3));

/**************WITHOUT  %TYPE *******************************/

DECLARE
	V_GRNO NUMBER(3);
	V_NAME VARCHAR2(3);
	V_AGE NUMBER(3);
	V_SEX CHAR(1);
	V_NO_OF_REC NUMBER(3);
BEGIN
	SELECT COUNT(*) INTO V_NO_OF_REC FROM STUD;


	FOR I IN 1..V_NO_OF_REC
	LOOP
		SELECT GRNO,NAME,AGE,SEX 
		INTO  V_GRNO,V_NAME,V_AGE,V_SEX 
		FROM STUD  WHERE GRNO=I;

		IF V_SEX='M' THEN
		   INSERT INTO STUD_MALE VALUES
		   (V_GRNO,V_NAME,V_AGE);
		ELSE
		   INSERT INTO STUD_FEMALE VALUES
		   (V_GRNO,V_NAME,V_AGE);
		END IF;
	END LOOP;
	
END;
/


/**************WITH %TYPE *******************************/

DECLARE
	V_GRNO STUD.GRNO%TYPE;
	V_NAME STUD.NAME%TYPE;
	V_AGE STUD.AGE%TYPE;
	V_SEX STUD.SEX%TYPE;
	V_NO_OF_REC NUMBER(3);
BEGIN
	SELECT COUNT(*) INTO V_NO_OF_REC FROM STUD;


	FOR I IN 1..V_NO_OF_REC
	LOOP
		SELECT GRNO,NAME,AGE,SEX 
		INTO  V_GRNO,V_NAME,V_AGE,V_SEX 
		FROM STUD  WHERE GRNO=I;

		IF V_SEX='M' THEN
		   INSERT INTO STUD_MALE VALUES
		   (V_GRNO,V_NAME,V_AGE);
		ELSE
		   INSERT INTO STUD_FEMALE VALUES
		   (V_GRNO,V_NAME,V_AGE);
		END IF;
	END LOOP;
	
END;
/

/**************WITH %ROWTYPE *******************************/

DECLARE
	V_STUD_RECO STUD%ROWTYPE;
	V_NO_OF_REC NUMBER(3);
BEGIN
	SELECT COUNT(*) INTO V_NO_OF_REC FROM STUD;


	FOR I IN 1..V_NO_OF_REC
	LOOP
		SELECT * INTO V_STUD_RECO
		FROM STUD  WHERE GRNO=I;

		IF V_STUD_RECO.SEX='M' THEN
		   INSERT INTO STUD_MALE VALUES
		   (V_STUD_RECO.GRNO,V_STUD_RECO.NAME,V_STUD_RECO.AGE);
		ELSE
		   INSERT INTO STUD_FEMALE VALUES
		   (V_STUD_RECO.GRNO,V_STUD_RECO.NAME,V_STUD_RECO.AGE);
		END IF;
	END LOOP;
	
END;
