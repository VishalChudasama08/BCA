DECLARE
	VB NUMBER(3);
	ORACLE NUMBER(3);
	PHP NUMBER(3);
	TOT NUMBER(3);
	PER NUMBER(5,2);
BEGIN
	VB:=&VB;
	ORACLE:=&ORACLE;
	PHP:=&PHP;

	TOT:=VB+ORACLE+PHP;
	PER:=TOT/3;

	DBMS_OUTPUT.PUT_LINE('SUBJECT		 MARKS');
	DBMS_OUTPUT.PUT_LINE('VB        	'||VB);
	DBMS_OUTPUT.PUT_LINE('ORACLE    	'||ORACLE);
	DBMS_OUTPUT.PUT_LINE('PHP       	'||PHP);
	DBMS_OUTPUT.PUT_LINE('TOTAL     	'||TOT);
	DBMS_OUTPUT.PUT_LINE('PERCENTAGE	'||PER);

		
	DBMS_OUTPUT.PUT_LINE('YOUR TOTAL MARKS IS '||TOT);
	DBMS_OUTPUT.PUT_LINE('YOUR PERCENTAGE IS '||PER);

	DBMS_OUTPUT.PUT('YOUR GRADE IS ');

	IF PER>=40 AND PER<=48 THEN
		DBMS_OUTPUT.PUT_LINE('PASS CLASS');
	END IF;

	IF PER>=49 AND PER<=60 THEN
		DBMS_OUTPUT.PUT_LINE('SECOND CLASS');
	END IF;

	IF PER>=61 AND PER<=70 THEN
		DBMS_OUTPUT.PUT_LINE('FIRST CLASS');
	END IF;

	IF PER>=71 THEN
		DBMS_OUTPUT.PUT_LINE('DISTINCTION');
	END IF;
END;
