SET SERVEROUTPUT ON;

/****BLOCK TO DO ADDITION,SUBTRACTION,
	MULTIPLICATION AND DIVISION OF TWO NUMBER*****/

DECLARE
	A NUMBER;
	B NUMBER;
	ADD NUMBER;
	SUB NUMBER;
	MUL NUMBER;
	DIV NUMBER;
BEGIN
	A:=&A;
	B:=&B;
	ADD:=A+B;
	SUB:=A-B;
	MUL:=A*B;
	DIV:=A/B;
	DBMS_OUTPUT.PUT_LINE('ADDITION OF A+B='||ADD);
	DBMS_OUTPUT.PUT_LINE('SUBTRACTIONTION OF A-B='||SUB);
	DBMS_OUTPUT.PUT_LINE('MULTIPLICATION OF A*B='||MUL);
	DBMS_OUTPUT.PUT_LINE('DIVISION OF A/B='||ROUND(DIV,2));
END;
