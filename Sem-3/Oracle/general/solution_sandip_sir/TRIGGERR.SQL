 CREATE TABLE MARKS
 (GRNO NUMBER(3),
 SUB1 NUMBER(3),
 SUB2 NUMBER(3),
 SUB3 NUMBER(3),
 TOT NUMBER(3),
 PER NUMBER(5,2));

/********************************/

CREATE OR REPLACE TRIGGER T_MARKS
BEFORE INSERT OR UPDATE ON marks
FOR EACH ROW
DECLARE
	V_SUB1 NUMBER(3);
	V_SUB2 NUMBER(3);
	V_SUB3 NUMBER(3);
	V_TOT NUMBER(3);
	V_PER NUMBER(5,2);
BEGIN
	V_SUB1:=:NEW.SUB1;
	V_SUB2:=:NEW.SUB2;
	V_SUB3:=:NEW.SUB3;
	V_TOT:=V_SUB1+V_SUB2+V_SUB3;
	V_PER:=V_TOT/3;

	/***INSERTING TOTAL AND PER IN THE TABLE**/
	:NEW.TOT:=V_TOT;
	:NEW.PER:=V_PER;
	
END;
