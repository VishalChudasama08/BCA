/*******CONVERT RUPEE INTO DOLLAR AND POUND***************/

DECLARE
	RUPEE NUMBER;
	DOLLAR NUMBER;
	POUND NUMBER;
BEGIN
	RUPEE:=&RUPEE;
	DOLLAR:=RUPEE/50;
	POUND :=RUPEE/80;
	DBMS_OUTPUT.PUT_LINE(RUPEE|| ' RUPEES = ' || DOLLAR || ' DOLLARS');
	DBMS_OUTPUT.PUT_LINE(RUPEE ||' RUPEES = ' || POUND ||' POUND');
END;
