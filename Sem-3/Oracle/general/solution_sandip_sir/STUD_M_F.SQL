CREATE TABLE STUD_FEMALE
(GRNO NUMBER(3),
NAME VARCHAR2(30),
AGE NUMBER(3));


/***********PLSQL BLOCK*********************/
DECLARE
	CURSOR v_stud_cur is select * from stud;
BEGIN
	
	FOR v_stud_rec IN v_stud_cur
	LOOP
		IF V_stud_rec.sex='M' THEN
		   INSERT INTO STUD_MALE VALUES
		   (V_GRNO,V_NAME,V_AGE);
		ELSE
		   INSERT INTO STUD_FEMALE VALUES
		   (V_GRNO,V_NAME,V_AGE);
		END IF;
	END LOOP;
	
END;
