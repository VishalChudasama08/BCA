SELECT PM.DESCRIPTION || ' WORTH RS. ' ||
SUM (SOD.QTY_ORDERED*SOD.PRODUCT_RATE) ||
' WAS ORDERED IN THE MONTH OF ' ||
TO_CHAR(SO.S_ORDER_DATE,'MONTH') FROM
PRODUCT_MASTER PM, SALES_ORDER_DETAILS SOD,
SALES_ORDER SO 
WHERE PM.PRODUCT_NO=SOD.PRODUCT_NO
AND SOD.S_ORDER_NO=SO.S_ORDER_NO
GROUP BY PM.DESCRIPTION, SO.S_ORDER_DATE;