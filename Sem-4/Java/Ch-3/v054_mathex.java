// java program to show the math class methods...
public class v054_mathex {
    public static void main(String[] args) {
        System.out.println("sin(1.57): " + Math.sin(1.57));
        System.out.println("asin(1.0): " + Math.asin(1.0));
        System.out.println("2 power 3: " + Math.pow(2, 3));
        System.out.println("square root of 25: " + Math.sqrt(25));
        System.out.println("Ceiling value of 5.2: " + Math.ceil(5.2));
        System.out.println("floor value of 6.7: " + Math.floor(6.7));
        System.out.println("minimum of 12&34: " + Math.min(12, 34));
    }
}
